% 单组元推进1N推力器、5N推力器的推力与比冲
% [F1,Isp1,F5,Isp5] = sengine(P)
% P为储箱压力，单位MPa
% 输出1N推力器的推力与比冲F1,Isp1
%     5N推力器的推力与比冲F5,Isp5
function [F1,Isp1,F5,Isp5] = sengine(P)
F1 = -0.0365*P.^3 +0.0185*P.^2 + 1.0383*P + 0.0551;
Isp1 = -111.62*P.^2 + 398.2*P + 1754.2;
F5 = -0.1164*P.^3 + 0.1837*P.^2 + 3.6608*P + 0.0944;
Isp5 = 175.38*P.^3 - 806.43*P.^2 + 1287.6*P + 1358.1;